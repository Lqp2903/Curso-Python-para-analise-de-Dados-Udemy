# Vamos trabalhar com o comando Input!
# O comando input() recebe como uma String que tem como objetivo auxiliar o usuário, geralmente informando que tipo de dado o usuário irá preencher.

# Exemplo 2 - Formulário de cadastro de cliente
Razao = input ("Qual sua razão social?")
CNPJ = input ("Qual seu CNPJ?")
IE = input ("Qual sua Inscrição Estadual?")
Telefone = input ("Informe seu telefone:")
Tamanho = input ("Informe o tamanho da sua empresa:")
Segmento = input ("Qual o segmento?")
print ("Por gentileza, confirme os dados informados:")
print ("A razão social da sua empresa é:", Razao)
print ("O CNPJ da sua empresa é:", CNPJ)
print ("A Inscrição Estadual é", IE)
print ("O telefone para contato é", Telefone)
print ("O tamanho da empresa é", Tamanho)
print ("O segmento da empresa é", Segmento)
print ("Fim")