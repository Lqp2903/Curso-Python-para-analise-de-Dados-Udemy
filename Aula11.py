# Vamos trabalhar com o comando Input!
# O comando input() recebe como uma String que tem como objetivo auxiliar o usuário, geralmente informando que tipo de dado o usuário irá preencher.

# Exemplo 1 - Formulário de identidade
Nome = input("Qual seu nome completo?")
Idade = input("Qual sua idade?")
Endereco = input ("Qual é o seu endereço?")
Escolaridade = input ("Qual sua escolaridade?")
print ('Seu nome é', Nome)
print ('Sua idade é', Idade)
print ('Seu endereço é', Endereco)
print ('Sua escolaridade é', Escolaridade)
print("Fim")