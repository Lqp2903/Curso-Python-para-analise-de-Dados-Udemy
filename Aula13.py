# Trabalhando com operadores lógicos
# São eles: And = 'and', Ou = 'or', Negação - 'not'

# Exemplo 1
print ("8 é maior que 74 e 74 é menor que 8:", 8 > 74 and 8 < 74)
print ("-1 é maior que 0 ou -1 é menor que 0:", -1 > 0 or -1 < 0)
print ("200 é igual a 200 e são diferentes", 200 == 200 and 200 != 200)

