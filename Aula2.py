# Nessa aula vamos trabalhar com alguns operadores matemáticos!

# Soma
print (1+1+1+1)
print (-1+1)

# Subtração
print(0-1)
print (11-1-0-2)

# Multiplicação
print (4*4)
print (10*100)

# Divisão
print (4/2)
print (400/15)

# Exponenciação
print (4**2)
print (8**4)

# Resto da divisão
print (3%4)
print (4%5)

# Divisão de chão
print (3//4)
print (50//8)

# Priorização: tudo o que está dentro do parenteses tem prioridade de execução. Na regra matemática, realizamos multiplicação e divisão, depois adição e subtração.
print ((2+2)*5)
print (2*5/5*(10+5)-1)
