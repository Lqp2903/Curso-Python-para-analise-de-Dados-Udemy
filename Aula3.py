# Nessa aula vamos nomear variáveis!

# Variáveis de texto recebem STR
Nome_1 = "Laura"
Nome_2 = str ("Quadros")
Nome_3 = "Piekatoski"
print (Nome_1,Nome_2,Nome_3)

# Variáveis numéricas recebem INT (quando for número inteiro)
Idade_1 = int (27)
Idade_2 = int (28)
Idade_3 = (29)
print (Idade_1, Idade_2, Idade_3)

# Variáveis flutuantes recebem FLOAT (números não inteiros)
Peso_1 = float (80.05)
Peso_2 = float (70.1)
Peso_3 = (50.5)
print (Peso_1, Peso_2, Peso_3)

# Variáveis booleanas são condições de verdadeiras ou falsas
Condicao_1 = True
Condicao_2 = False
print (Condicao_1, Condicao_2)